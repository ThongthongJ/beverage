class Beer {
  final String name;
  final String tagline;
  final String description;
  final String image;
  final int price;
  final String first_brewed;
  final String contributed;

  Beer({
    required this.name,
    required this.tagline,
    required this.description,
    required this.image,
    required this.price,
    required this.first_brewed,
    required this.contributed,
  });
}
